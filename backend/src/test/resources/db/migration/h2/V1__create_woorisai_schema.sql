CREATE TABLE woorisai.participant
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY,
    slot         SMALLINT                   NOT NULL,
    display_name VARCHAR(30)                NOT NULL,
    created_at   TIMESTAMP WITH TIME ZONE   NOT NULL,
    CONSTRAINT participant_pk PRIMARY KEY (id),
    CONSTRAINT participant_slot_uq UNIQUE (slot),
    CONSTRAINT participant_display_name_uq UNIQUE (display_name),
    CONSTRAINT participant_slot_ck CHECK (slot = 1 OR slot = 2),
    CONSTRAINT participant_display_name_ck CHECK (char_length(trim(display_name)) > 0)
);

CREATE TABLE woorisai.participant_credential
(
    participant_id BIGINT                   NOT NULL,
    pin_hash       VARCHAR(255)             NOT NULL,
    updated_at     TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT participant_credential_pk PRIMARY KEY (participant_id),
    CONSTRAINT participant_credential_participant_fk
        FOREIGN KEY (participant_id) REFERENCES woorisai.participant (id) ON DELETE RESTRICT,
    CONSTRAINT participant_credential_pin_hash_ck CHECK (char_length(trim(pin_hash)) > 0)
);

CREATE TABLE woorisai.relationship_score
(
    id                    BIGINT GENERATED BY DEFAULT AS IDENTITY,
    source_participant_id BIGINT                   NOT NULL,
    target_participant_id BIGINT                   NOT NULL,
    current_score         SMALLINT                 NOT NULL,
    updated_at            TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT relationship_score_pk PRIMARY KEY (id),
    CONSTRAINT relationship_score_source_uq UNIQUE (source_participant_id),
    CONSTRAINT relationship_score_target_uq UNIQUE (target_participant_id),
    CONSTRAINT relationship_score_id_source_uq UNIQUE (id, source_participant_id),
    CONSTRAINT relationship_score_source_fk
        FOREIGN KEY (source_participant_id) REFERENCES woorisai.participant (id) ON DELETE RESTRICT,
    CONSTRAINT relationship_score_target_fk
        FOREIGN KEY (target_participant_id) REFERENCES woorisai.participant (id) ON DELETE RESTRICT,
    CONSTRAINT relationship_score_participants_ck
        CHECK (source_participant_id <> target_participant_id),
    CONSTRAINT relationship_score_value_ck CHECK (current_score BETWEEN 0 AND 100)
);

CREATE TABLE woorisai.score_change
(
    id                    BIGINT GENERATED BY DEFAULT AS IDENTITY,
    relationship_score_id BIGINT                   NOT NULL,
    changed_by_id         BIGINT                   NOT NULL,
    delta                 SMALLINT                 NOT NULL,
    resulting_score       SMALLINT                 NOT NULL,
    reason                VARCHAR(200),
    created_at            TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT score_change_pk PRIMARY KEY (id),
    CONSTRAINT score_change_outgoing_owner_fk
        FOREIGN KEY (relationship_score_id, changed_by_id)
            REFERENCES woorisai.relationship_score (id, source_participant_id) ON DELETE RESTRICT,
    CONSTRAINT score_change_delta_ck CHECK (delta BETWEEN -100 AND 100 AND delta <> 0),
    CONSTRAINT score_change_result_ck CHECK (resulting_score BETWEEN 0 AND 100),
    CONSTRAINT score_change_reason_ck CHECK (reason IS NULL OR char_length(trim(reason)) > 0)
);

CREATE INDEX score_change_history_idx
    ON woorisai.score_change (relationship_score_id, created_at DESC, id DESC);

CREATE TABLE woorisai.score_change_comment
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY,
    score_change_id BIGINT                   NOT NULL,
    author_id       BIGINT                   NOT NULL,
    content         VARCHAR(500),
    created_at      TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT score_change_comment_pk PRIMARY KEY (id),
    CONSTRAINT score_change_comment_change_fk
        FOREIGN KEY (score_change_id) REFERENCES woorisai.score_change (id) ON DELETE RESTRICT,
    CONSTRAINT score_change_comment_author_fk
        FOREIGN KEY (author_id) REFERENCES woorisai.participant (id) ON DELETE RESTRICT,
    CONSTRAINT score_change_comment_content_ck
        CHECK (content IS NULL OR char_length(trim(content)) > 0)
);

CREATE INDEX score_change_comment_thread_idx
    ON woorisai.score_change_comment (score_change_id, created_at, id);

CREATE TABLE woorisai.diary_entry
(
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY,
    author_id  BIGINT                   NOT NULL,
    content    VARCHAR(1000)            NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE,
    CONSTRAINT diary_entry_pk PRIMARY KEY (id),
    CONSTRAINT diary_entry_author_fk
        FOREIGN KEY (author_id) REFERENCES woorisai.participant (id) ON DELETE RESTRICT,
    CONSTRAINT diary_entry_content_ck CHECK (char_length(trim(content)) > 0),
    CONSTRAINT diary_entry_timestamps_ck CHECK (updated_at IS NULL OR updated_at >= created_at)
